Yeah, most of that really.<br><br>
Events can generate messages, set objectives or instructions for any element, change the IFF of any element (e.g. can be used to wake up zombie elements by changing IFF from 1000 to 2), cause damage to any ship, or cause any ship to initiate a jump.<br>
Triggers include: Time, Damage, Destroyed, Jump, Launch, Dock, and Event. <br>
There's no branching constructs, and you can't trigger on "detection" per se, but I'm sure you geniuses will be able to cause plenty of trouble with the features that are supported.<br><br>
