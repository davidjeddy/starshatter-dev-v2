What kind of events? Like if a ship is destroyed, or damaged to a certain percentage? A certain subsystem is knocked offline? Something gets within a certain range of something else (reinforcements are sent in to defend a station, but the call only goes out after the station has detected the attacking force)? What can they affect? Can they change an element's orders?<br>
