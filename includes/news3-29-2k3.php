&nbsp;&nbsp;&nbsp;3/29/2003 - 9:00am EST - Wooa, milo went and did a change up to <a href="http://www.starshatter.com" target="_new" class="minilinks">http://www.starshatter.com</a>! He as a brand new layout that reflects what the final game interface will look like, new screen shots that show off some redisnged / new ships, new interface, and a peak at the universe map! PLUS alot more!<br>
<br>
&nbsp;&nbsp;&nbsp;From milo's post:
&nbsp;&nbsp;&nbsp;<i>I decided it was time to brighten the place up a bit. The new web design is also the new game design - all of the GUI screens in the game have been redesigned to use this style. So I really hope you all like it! <br>
I'd like to announce that I am officially no longer working entirely by myself on this project. Sean Moser, one of my best friends and an accomplished artist and designer, has joined the team as art director. Sean and I jointly developed the new GUI style. He is mainly working on the Marakan star fleet while I finish up the Terellian ships.<br>
Also included with the new website are fifteen new screenshots, and the beginnings of an actual background section (including more images of some of the new star systems).<br>
</i>
