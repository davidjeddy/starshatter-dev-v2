 Well, as you may expect, I'm behind schedule a bit. Sorry for the delay in getting the beta started. The internal QA process is taking a bit longer than I expected. Feature-wise, everything is finished except for the big one: multiplayer. Now it's just a matter of working through the million-and-one small problems that make the difference between a demo and a game.<br><br>

I've been working hard to get the new Highland campaign complete, which I consider a prerequesite for beta. However, everytime I add a new element to the campaign, I find half a dozen small bugs that need to be stamped out to allow the campaign to progress properly. I'm also learning that because of the dynamic nature of the "story" behind the campaign, getting proper and consistent feedback in the campaign screen is hugely important. Otherwise, you can't quite figure out what is going on around you.<br><br>

A Random Sampling of the Stuff Milo has bene Working On:<br><br>

* Sound effects for all GUI elements and ship functions<br>
* Fixing bugs in the promotion and medal requirements<br>
* Finalizing the art for all rank and medal insignia<br>
* Streamlining the GUI so that it flows more naturally<br>
* Added "events" to the mission builder<br>
* Fixed Crazy-Eddie's frigate defense bug<br>
* Fixed the auto skip so that it Actually Works (TM)<br>
* Each player-character now gets separate save games (necessary because unit assignments are limited by rank)<br>
* Added a START button that automatically loads the last save game (or starts a new campaign if no save game is found)<br>
* Fixed fighter AI so that it won't crash (as much) when trying to land<br>
* Starship damage is now persistent during all dynamic campaigns<br>
* Player now receives "command" experience points for actions performed by subordinates (otherwise you would never get another promotion after taking command of a carrier)<br>
* Improved handling of secret and reserve units in the campaign engine<br>
* Minefields<br><br>

Plus about a hundred other minor bugs. I fixed a dozen last night alone.<br><br>

Anyway, thanks for you continued patience. I will try to ensure that it was all worth the wait. <br><br>

